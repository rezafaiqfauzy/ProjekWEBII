                    <h4>Pesona Keindahan Gunung Rinjani</h4>
                     <ul class="post-info">
                        <li><a>Reza Faiq Fauzy</a></li>
                        <li><a>10 September 2021</a></li>
                      </ul>
                      <p>Anda pecinta alam dan penikmat gunung? Belum lengkap rasanya jika Anda belum merasakan sensasi mendaki di gunung yang terkenal sangat cantik akan pesona alamnya ini. Dengan ketinggian 3.726 mdpl dan terletak di utara Pulau Lombok, Gunung Rinjani merupakan gunung berapi kedua tertinggi di Indonesia. Masuk dalam kawasan Taman Nasional Gunung Rinjani dan dikelilingi oleh hutan dan semak belukar seluas 76.000 hektar merupakan pemandangan yang asri bagi Gunung Rinjani.

                      <br>Akses menuju Pulau Lombok selain dapat ditempuh melalui jalur darat menggunakan bus langsung Jakarta â€“ Mataram dengan menyeberang menggunakan kapal ferry dua kali (Selat Bali dan Selat Lombok), juga dapat ditempuh dengan menggunakan pesawat terbang.</br>
                      <br>Gunung Rinjani memiliki kawah dengan lebar sekitar 10 km dan terdapat danau kawah yang disebut danau Segara Anak dengan kedalaman sekitar 230 m. Dengan warna airnya yang membiru bagaikan anak lautan, air yang mengalir dari danau ini membentuk air terjun yang sangat indah dan mengalir melewati jurang yang curam. Banyak para pendaki yang memancing di danau ini karena banyak terdapat ikan mas dan mujair.</br>
                      
                      <br>Ada 2 jalur pendakian untuk mencapai Puncak Rinjani, yaitu Jalur Sembalun dan Jalur Senaru. Jalur Sembalun merupakan jalur favorit para pendaki karena meskipun treknya lebih panjang namun bisa menghemat 700 m ketinggian.</br>

                      <br>Di Jalur Sembalun, pendaki akan melalui hamparan padang savana yang sangat luas dan cantik. Ada 3 pos peristirahatan di jalur ini, dan selepas dari pos 3 pendaki akan menghadapi tanjakan terjal dengan kemiringan sekitar 60 derajat. Sedangkan di Jalur Senaru, pendaki akan melewati hutan tropis yang cukup lebat dan terjal. Sama halnya dengan Jalur Sembalun, jalur ini juga terdapat 3 pos peristirahatan sebelum nantinya sampai ke pos pelawangan yang biasa digunakan sebagai area perkemahan.</br>

                      <br>Pesona yang dimiliki oleh Gunung Rinjani nyaris sempurna sehingga tidak diragukan lagi jika Rinjani menjadi daya tarik yang mampu memikat minat para wisatawan mancanegara maupun nusantara untuk mendakinya. Dan mendaki Gunung Rinjani tentunya akan menjadi kenangan dan pengalaman hidup yang tidak akan terlupakan.</br>
