                      <h4>Sejarah Gunung Krakatau</h4></a>
                      <ul class="post-info">
                        <li><a>Adam Muhamad Iqbal</a></li>
                        <li><a>13 November 2021</a></li>
                      </ul>
                      <p>Krakatau (atau Rakata) adalah kepulauan vulkanik yang masih aktif dan berada di Selat Sunda, antara Pulau Jawa dan Sumatra. Nama ini juga disematkan pada satu puncak gunung berapi di sana (Gunung Krakatau) yang sirna karena letusan kataklismik pada tanggal 26-27 Agustus 1883. Pada tahun 2019, kawasan yang sekarang merupakan cagar alam ini memiliki empat pulau kecil: Pulau Rakata, Pulau Anak Krakatau, Pulau Sertung, dan Pulau Panjang (Rakata Kecil). Berdasarkan kajian geologi, semua pulau ini berasal dari sistem gunung berapi tunggal Krakatau yang pernah ada di masa lalu.
                      <br><br>Krakatau dikenal dunia karena letusan yang sangat dahsyat pada tahun 1883. Awan panas dan tsunami yang diakibatkannya menewaskan sekitar 36.000 jiwa. Sampai sebelum tanggal 26 Desember 2004, tsunami ini adalah yang terdahsyat di kawasan Samudera Hindia. Suara letusan itu terdengar sampai di Alice Springs, Australia dan Pulau Rodrigues dekat Afrika, 4.653 kilometer. Daya ledaknya diperkirakan mencapai 30.000 kali bom atom yang diledakkan di Hiroshima dan Nagasaki di akhir Perang Dunia II.
                      <br><br>Tercatat bahwa letusan Gunung Krakatau adalah bencana besar pertama di dunia setelah penemuan telegraf bawah laut. Kemajuan tersebut, sayangnya belum diimbangi dengan kemajuan di bidang geologi. Para ahli geologi saat itu bahkan belum mampu memberikan penjelasan mengenai letusan tersebut. Gunung Krakatau yang meletus, getarannya terasa sampai Eropa.</p>
