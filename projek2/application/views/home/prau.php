                     <h4>Menikmati keindahan Gunung Prau</h4>
                        <ul class="post-info">
                        <li><a>Raul</a></li>
                        <li><a>8 Oktober 2021</a></li>
                      </ul>
                     <p> Gunung Prahu, lebih dikenal dengan sebutan Gunung Prau, memiliki ketinggian 2.565 mdpl. Gunung ini terletak di kawasan Dataran Tinggi Dieng, Jawa Tengah.
                      Gunung Prau berada di antara tiga kabupaten, yaitu Kabupaten Batang, Kendal, dan Wonosobo. 
                      <br>Puncak Gunung Prau merupakan padang rumput luas yang memanjang dari barat ke timur. Bukit-bukit kecil dan sabana, dengan sedikit pepohonan dapat kita jumpai di puncak</br>
                      <br>Menurut seorang pendaki Alif Fauzan, Gunung Prau merupakan â€œsurga duniaâ€ dari Wonosobo. Saat di puncak, Alif dapat melihat keindahan dua gunung yang berdampingan, yaitu Gunung Sindoro dan Sumbing.
                      <br>"Mendaki Guning Prahu (Prau) cukup cepat, karena dari basecamp ke puncak hanya memakan waktu tiga jam saja, bahkan bisa lebih cepat. Hal yang bikin kangen naik Gunung Prahu melihat pemandangan dua gunung berdampingan dan sunrise yang menakjubkan," ujar Alif</br>
                      <br>Menurutnya, Gunung Prau merupakan jalur pendakian yang cocok bagi pendaki pemula. Jalur yang landai dan tidak memakan waktu banyak untuk sampai di puncak menjadi alasannya.
                      <br>"Track-nya landai dan cocok bagi pemula. Gunung Prahu juga keren. Menurut saya ini merupakan surga dunia dari Wonosobo," katanya.</br>
                      <br>Dihubungi terpisah, seorang pendaki asal Jakarta, Aldi, juga pernah menjejakan kakinya di Gunung Prau. Berawal dari kejenuhan aktivitasnya, bersama temannya, Aldi berhasil mencapai puncak melalui jalur Patak Banteng.</br>
                      <br>"Kalau track-nya via Patak Banteng cape banget, soalnya nanjak terus dari pos satu sampai puncak. Enggak ada jalan bonusnya. Tapi cepat, cuma tiga jam sudah sampai," kata Aldi.</br>
                      <br>Dalam pendakiannya, ia dituntut untuk menolong sesama dan menurunkan ego.
                      <br>"Kaki teman saya keram, sampai akhirnya kami packing ulang carrir (tas gunung) yang tadinya tiga menjadi dua. Saya turun ke basecamp lagi untuk menaruh carrir satunya," jelasnya.</br>
                      <br>Sama halnya dengan Alif, Aldi merasa takjub dengan keindahan sunrise di puncak Gunung Prau dan dedaunan, yang beku seperti diselimuti es batu.</br>
                      <br>"Hal yang tak terlupakan bisa memegang daun yang tertutup es dan melihat view yang indah banget pas sunrise. Cerita dan pemandangannya bikin kangen dan selalu ada alasan untuk kembali," ujarnya.
                     </br>
                      <br>Hal senada di ungkapkan Imelda. Track-nya yang mudah sangat direkomendasikan bagi pendaki pemula. Selain itu, pemandangan yang mampu memanjakan mata merupakan bonus menhilangkan rasa lelah selama pendakian.</br>
                      <br>"Di puncak view-nya bagus banget, kita bisa lihat Gunung Sindoro dan Gunung Sumbing langsung di depan tenda. Paling kesulitannya kalau sudah masuk musim hujan, dari pos awal sampai pos satu biasanya perkebunan warga jalurnya licin banget," katanya.</br>
                      <br>Meski tergolong cepat dan jalur yang landai, pendaki memang harus tetap hati-hati dan membawa perlengkapan selama mendaki. Selain itu, pendaki seharusnya memerhatikan kondisi fisik sebelum mendaki. Jadi disarankan untuk latihan fisik terlebih dahulu.</br>
                      <br>Kemudian, mematuhi segala aturan yang ada dan tentunya jangan melakukan pendakian seorang diri. Baiknya ditemani orang lain untuk mengantisipasi hal-hal yang tidak diinginkan.</br>
                    </p>


